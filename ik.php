<div class="container">
    <h4>1. Ringkasan Pekerjaaan</h4>
    <p>Proses pemeriksaan hasil jahitan (skin) sesuai dengan approval sample dari Buyer</p>
    <h4>2. Tugas Dan Tanggung Jawab</h4>
    <li>Memastikan kualitas hasil sewing 100% PASS sesuai dengan instruksi Buyer sebelum masuk ke departemen finishing. </li>
    <li>Bertanggung jawab untuk memisahkan hasil sewing yang pass dengan yang riject dengan di beri tanda.</li>
    <li>Membuat laporan untuk hasil pemeriksaan hasil sewing. </li>
    <h4>3. Langkah Detail Pekerjaan</h4>
    <li>Siapkan sample approval/red seal</li>
    <li>Pastikan jenis bahan dan warna boneka sesuai dengan approval sample</li>
    <li>Pastikan jarak jahitan sesuai dengan SPI ( Stich per inch) standar, untuk bahan tebal 9-10, untuk bahan tipis 10-11/inch.</li>
    <li>Pastikan batas jahitan untuk bahan tebal kurang lebih 6 mm toleransinya kurang lebih 2 mm, untuk bahan tipis kurang lebih 5 mm untuk toleransinya kurang lebih 1-2 mm.</li>
    <li>Pastikan awal dan akhir jahitan di stich balik.</li>
    <li>Pastikan urutan label, posisi label benar sesuai dengan sample approval</li>
    <li>Melakukan pemeriksan pada warna, bahan, jahitan embroidery, bentuk boneka, di bandingkan dengan approval sample (read seal)</li>
    <li>Pastikan jenis bahan, ukuran benang, warna benang sesuai dengan sample approval.</li>
    <li>Pasikan penempatan produk yang sudah di periksa dengan benar (box biru yang bertuliskan pass/ok dan box merah bertuliskan riject).</li>
    <li>Catat hasil laporan pemeriksaan pada form di aplikasi ini.</li>
    <li>selanjutnya boneka di distribusikan ke departemen berikutnya.</li>
    <h4>4 Hal Yang Perlu Di Perhatikan</h4>
    <li>Pastikan jenis bahan dan warna boneka sesuai dengan approval sample </li>
    <li>Untuk setiap komponen yang akan di proses di sewing harus ada identitas jelas seperti: tanggal potong, nama komponen, nama item, nomor kontak kerja dan nomor PO material</li>
    <li>Pastikan jarak jahitan sesuai dengan
    <li>Pasyikan awal dan akhir jahitan di stich balik</li>
    <li>pastikan batas jahitan untuk bahan tebal dan tipis kurang lebil 5 mm toleransinya kurang lebih 1-2 mm</li>
    <li>Definisi riject atau BS adalah : Jahitan terbuka, salah label, posisi tangan, kaki, ekor, telinga terbalik atau salah posisi, material bolong, dan embroidery rusak.</li>
